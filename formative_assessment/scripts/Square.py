#function to define the forward motion 
def move_in_x(value):

    twist=Twist()
    global deg,x_value,y_value

    rate=rospy.Rate(10)
    while not rospy.is_shutdown():
        distance =abs(x_value-value)
        if distance>0.1:
            twist.linear.x=2.0
            cmd_vel_pub.publish(twist)
        else:
            twist.linear.x=0.0
            cmd_vel_pub.publish(twist)
            break
    
        rate.sleep()
    print(deg,x_value,y_value)        

def move_in_y(value):

    twist=Twist()
    global deg,x_value,y_value

    rate=rospy.Rate(10)
    while not rospy.is_shutdown():
        distance =abs(y_value-value)
        if distance>0.1:
            twist.linear.x=2.0
            cmd_vel_pub.publish(twist)
        else:
            twist.linear.x=0.0
            cmd_vel_pub.publish(twist)
            break

        rate.sleep()
    print(deg,x_value,y_value)  

def rotate(value):

    twist=Twist()
    global deg

    rate=rospy.Rate(10)
    
    while not rospy.is_shutdown():
        theta_diff=value-deg
        theta_diff=(theta_diff+3.14)%(2*3.14)-3.14

        if abs(theta_diff)>0.01:
            twist.linear.x=0.0
            twist.angular.z=0.5432*theta_diff/abs(theta_diff)
            cmd_vel_pub.publish(twist)
                    
        else :
            twist.linear.x=0.0
            twist.angular.z=0.0
            cmd_vel_pub.publish(twist)            
            break
        rate.sleep()
    print(deg)    
    
    
def draw_square():
    move_in_x(8)
    rotate(1.57)
    move_in_y(8)
    rotate(3.14)
    move_in_x(5.54)
    rotate(-1.57)
    move_in_y(5.54)
    rotate(0.0)

